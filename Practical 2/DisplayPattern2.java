public class DisplayPattern2 {
    public static void main(String[] args) {
        System.out.println("    " + ".=\"  \"=.");
        System.out.println("  " + "/" + "\t     " + "\\");
        System.out.println(" |" + "   O" + " \t" + "  O  " + " |") ;
        System.out.println(" \\" + "     " +"/" + "\\" + "     /") ;
        System.out.println(" / " + "\'" + " -=\\" +"/=- " + "\' " + "\\");
        System.out.println("/" + " /" + "\t " + "    \\" + " \\" + "\t   " + "CHIP CHIP!");
        System.out.println("\\" + "/" + "\\" + "\t     " + "/" + "\\/");
        System.out.println("   " + "\'.  " + "\t" + "   ." + "\'");
        System.out.println("    " + "/|\\ " + " /|\\");
    }
    
}