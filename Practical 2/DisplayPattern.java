public class DisplayPattern {
    public static void main(String[] args) {
        System.out.println("( \\__/)  I am an");
        System.out.print("(  @w@)" );
        System.out.println(" \tOOO\tW  \tW    L");
        System.out.print("/)  W )\t       O" );
        System.out.println("   O  \t  W W W\t     L");
        System.out.print("  ^  ^" );
        System.out.println("  \tOOO  \t   W W\t     L L L");
    }
}

